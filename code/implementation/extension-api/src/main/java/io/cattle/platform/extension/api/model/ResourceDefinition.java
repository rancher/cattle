package io.cattle.platform.extension.api.model;

public class ResourceDefinition {

    String name;

    public ResourceDefinition(String name) {
        super();
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return name;
    }

}
